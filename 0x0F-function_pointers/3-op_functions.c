#include "3-calc.h"

/**
 * op_add - calculate sum
 * @a: first int
 * @b: second int
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate subtraction
 * @a: first int
 * @b: second int
 * Return: subtraction
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates multiplications
 * @a: first int
 * @b: second int
 * Return: multply
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate division
 * @a: first int
 * @b: second int
 * Return: result of the division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate remainder
 * @a: first int
 * @b: second int
 * Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
